package com.huawei.bloomfate.model;

public class Value {

    private double general;

    private double photo;

    private double edu;

    private double job;

    public double getGeneral() {
        return general;
    }

    public void setGeneral(double general) {
        this.general = general;
    }

    public double getPhoto() {
        return photo;
    }

    public void setPhoto(double photo) {
        this.photo = photo;
    }

    public double getEdu() {
        return edu;
    }

    public void setEdu(double edu) {
        this.edu = edu;
    }

    public double getJob() {
        return job;
    }

    public void setJob(double job) {
        this.job = job;
    }
}
